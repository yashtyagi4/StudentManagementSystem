    //Method to get the courses from database
    public void courses(){
        //Database Server Info
        final String url      = "jdbc:mysql://127.0.0.1:3306/studentmanagement";
        final String root     = "root";
        final String pass     = "password";
        
        
        try {
            //Setting up the database
            Class.forName("com.mysql.jdbc.Driver"); //MySQL Driver
            con = DriverManager.getConnection(url, root, pass); //Connecting to the server
            pst_course = con.prepareStatement("select * from course");
            rs = pst_course.executeQuery();
            
            txtCourse.removeAllItems();     //Emptying anything old
   
            //Extracting the data from course table in the database
            while (rs.next()){
            txtCourse.addItem(rs.getString(2)); //courseName
        }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
