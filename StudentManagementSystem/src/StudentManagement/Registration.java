    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Database Credentials
        final String url      = "jdbc:mysql://127.0.0.1:3306/studentmanagement";
        final String root     = "root";
        final String pass     = "password";

        try {

            //Checking if the provided information is empty
            if(txtFirst.getText().isEmpty() || txtLast.getText().isEmpty() || txtGender.getText().isEmpty() 
                    || txtTele.getText().isEmpty() || txtAddress.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(this, "Empty information was provided somewhere");
            }
            else{ //If not
                // The data user provide will be assigned to variables
                String firstName     = txtFirst.getText();
                String lastName      = txtLast.getText();
                String gender        = txtGender.getText();
                String telephone     = txtTele.getText();
                String address       = txtAddress.getText();
                String course        = txtCourse.getSelectedItem().toString();
                String studentClass  = txtClass.getSelectedItem().toString();
                

                //Setting up the database
                Class.forName("com.mysql.jdbc.Driver"); //MySQL Driver
                con = DriverManager.getConnection(url, root, pass); //Connecting to the server

                //Check username and password, if they both are correct
                pst = con.prepareStatement("insert into registration(first_name, last_name, gender, course, student_class, telephone, address)values(?,?,?,?,?,?,?)");

                //Check the statement and put the info
                pst.setString(1, firstName);
                pst.setString(2, lastName);
                pst.setString(3, gender);
                pst.setString(4, course);
                pst.setString(5, studentClass);
                pst.setString(6, telephone);
                pst.setString(7, address);
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Student was registered"); //Display success message
                
                //Clear form
                txtFirst.setText("");
                txtLast.setText("");
                txtGender.setText("");
                txtTele.setText("");
                txtAddress.setText("");
                txtCourse.setSelectedItem(-1);
                txtClass.setSelectedItem(-1);
                txtFirst.requestFocus();
            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

